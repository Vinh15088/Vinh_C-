#include<bits/stdc++.h>
using namespace std;

int main(){
    freopen("Tutorial5_1.cpp", "r", stdin);
    string c, s;
    while((c= getchar()) != EOF){ 
    /*
    EOF: end of file. khi con tro cua tep tro vao cuoi tep
    getchar(): doc ki tu tiep theo
    */
        c = s;
    } 
    cout << reverse(s);
    fclose(stdin);
    return 0;
}